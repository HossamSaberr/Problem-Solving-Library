int h , w;
char grid[1005][1005];
int dp[1005][1005];
const int MOD = 1e9 + 7;

int idk(int i , int j) {
    if (i >= h || j >= w || grid[i][j] == '#')
        return 0;
    if (i == h - 1 && j == w - 1)
        return 1;
    int &ret = dp[i][j];
    if(~ret)
        return ret;
    int ch1 = idk(i + 1 , j);
    int ch2 = idk(i , j + 1);
    return ret = (ch1 + ch2) % MOD;
}

